
const body = document.querySelector('body')


function toggleMode() {
    body.classList.toggle('.wrapper')
    const wrapper = document.querySelector('.wrapper')
    const round = document.querySelector('.round')
}
if (wrapper.style.backgroundImage = 'url("")') {
} else {
    wrapper.style.backgroundImage = 'url("")'
}


if (round.style.backgroundImage = 'url("")') {
} else {
    round.style.backgroundImage = 'url("")'
}




window.addEventListener('load', toggleMode) 
